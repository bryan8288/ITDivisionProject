package com.example.felix.stuger.Model;

public class Leaderboard {
    private String LeaderboardName;
    private String LeaderboardTitle;
    private int LeaderboardScore;

    public String getLeaderboardName() {
        return LeaderboardName;
    }

    public void setLeaderboardName(String leaderboardName) {
        LeaderboardName = leaderboardName;
    }

    public String getLeaderboardTitle() {
        return LeaderboardTitle;
    }

    public void setLeaderboardTitle(String leaderboardTitle) {
        LeaderboardTitle = leaderboardTitle;
    }

    public int getLeaderboardScore() {
        return LeaderboardScore;
    }

    public void setLeaderboardScore(int leaderboardScore) {
        LeaderboardScore = leaderboardScore;
    }
}
