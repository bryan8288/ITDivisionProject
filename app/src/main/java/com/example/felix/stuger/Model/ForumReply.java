package com.example.felix.stuger.Model;

public class ForumReply {
    private String ForumReplyID;
    private String ForumReplyDesc;
    private String ForumReplyName;
    private int ForumScore;

    public String getForumReplyID() {
        return ForumReplyID;
    }

    public void setForumReplyID(String forumReplyID) {
        ForumReplyID = forumReplyID;
    }

    public String getForumReplyDesc() {
        return ForumReplyDesc;
    }

    public void setForumReplyDesc(String forumReplyDesc) {
        ForumReplyDesc = forumReplyDesc;
    }

    public String getForumReplyName() {
        return ForumReplyName;
    }

    public void setForumReplyName(String forumReplyName) {
        ForumReplyName = forumReplyName;
    }

    public int getForumScore() {
        return ForumScore;
    }

    public void setForumScore(int forumScore) {
        ForumScore = forumScore;
    }
}
